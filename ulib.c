char*
strcpy(char *s, const char *t)
{
  char *os;

  os = s;
  while((*s++ = *t++) != 0)
    ;
  return os;
}

int
strcmp(const char *p, const char *q)
{
  while(*p && *p == *q)
    p++, q++;
  return (uchar)*p - (uchar)*q;
}

uint
strlen(const char *s)
{
  int n;

  for(n = 0; s[n]; n++)
    ;
  return n;
}

void*
memset(void *dst, int c, uint n)
{
  stosb(dst, c, n);
  return dst;
}

char*
strchr(const char *s, char c)
{
  for(; *s; s++)
    if(*s == c)
      return (char*)s;
  return 0;
}

char*
gets(char *buf, int max)
{
  int i, cc;
  char c;

  for(i=0; i+1 < max; ){
    cc = read(0, &c, 1);
    if(cc < 1)
      break;
    buf[i++] = c;
    if(c == '\n' || c == '\r')
      break;
  }
  buf[i] = '\0';
  return buf;
}

int
stat(const char *n, struct stat *st)
{
  int fd;
  int r;

  fd = open(n, O_RDONLY);
  if(fd < 0)
    return -1;
  r = fstat(fd, st);
  close(fd);
  return r;
}

int
atoi(const char *s)
{
  int n;

  n = 0;
  while('0' <= *s && *s <= '9')
    n = n*10 + *s++ - '0';
  return n;
}

void*
memmove(void *vdst, const void *vsrc, int n)
{
  char *dst;
  const char *src;

  dst = vdst;
  src = vsrc;
  while(n-- > 0)
    *dst++ = *src++;
  return vdst;
}

static void
sputc(char* dst, char c, int* index)
{
  dst[(*index)++] = c;
}

static void
sprintint(char* dst, int xx, int base, int sgn,int* index)
{
  static char digits[] = "0123456789ABCDEF";
  char buf[16];
  int i, neg;
  uint x;

  neg = 0;
  if(sgn && xx < 0){
    neg = 1;
    x = -xx;
  } else {
    x = xx;
  }

  i = 0;
  do{
    buf[i++] = digits[x % base];
  }while((x /= base) != 0);
  if(neg)
    buf[i++] = '-';

  while(--i >= 0)
    sputc(dst, buf[i],index);
}

// Print to the given fd. Only understands %d, %x, %p, %s.
void
sprintf(char* dst, const char *fmt, ...)
{
  char *s;
  int c, i, state;
  uint *ap;
  int index = 0;
  state = 0;
  ap = (uint*)(void*)&fmt + 1;
  for(i = 0; fmt[i]; i++){
    c = fmt[i] & 0xff;
    if(state == 0){
      if(c == '%'){
        state = '%';
      } else {
        sputc(dst, c,&index);
      }
    } else if(state == '%'){
      if(c == 'd'){
        sprintint(dst, *ap, 10, 1,&index);
        ap++;
      } else if(c == 'x' || c == 'p'){
        sprintint(dst, *ap, 16, 0,&index);
        ap++;
      } else if(c == 's'){
        s = (char*)*ap;
        ap++;
        if(s == 0)
          s = "(null)";
        while(*s != 0){
          sputc(dst, *s,&index);
          s++;
        }
      } else if(c == 'c'){
        sputc(dst, *ap,&index);
        ap++;
      } else if(c == '%'){
        sputc(dst, c,&index);
      } else {
        // Unknown % sequence.  Print it to draw attention.
        sputc(dst, '%',&index);
        sputc(dst, c,&index);
      }
      state = 0;
    }
  }
  dst[index] = '\0';
}




